document.write("<center><h1>do while loop</h1></center>")

document.write("<h3> increment loop </h3> <br>")

var a = 1
do{
   document.write(a + " ) this is increment loop <br>")
    a++
}while(a <= 10)

document.write("<h3> decrement loop </h3> <br>")
document.write("<hr>")

var b = 10
do{
  document.write(b +  ") this is decrement loop <br>")
  b--
}while(b >= 1)